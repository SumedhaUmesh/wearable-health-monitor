int MockPpgDriver::init()
{
	index_ = 0;
	initialized_ = true;
	return 0;
}

int MockPpgDriver::read_sample(uint32_t& out_sample)
{
	if (!initialized_) {
		return -EINVAL;
	}
	out_sample = kPpgSamples[index_];
	const unsigned n = sizeof(kPpgSamples) / sizeof(kPpgSamples[0]);
	index_ = (index_ + 1U) % n;
	return 0;
}
